<script>
    // Privacy Policy Modal functionality
    document.addEventListener('DOMContentLoaded', function() {
        const privacyModal = document.getElementById('privacyModal');
        const privacyLink = document.getElementById('privacyLink');
        const privacyModalClose = document.getElementById('privacyModalClose');
        const privacyModalBackdrop = document.getElementById('privacyModalBackdrop');

        if (!privacyModal || !privacyLink || !privacyModalClose || !privacyModalBackdrop) {
            return;
        }

        function openPrivacyModal() {
            privacyModal.classList.remove('hidden');
            document.body.style.overflow = 'hidden';
        }

        function closePrivacyModal() {
            privacyModal.classList.add('hidden');
            document.body.style.overflow = '';
        }

        privacyLink.addEventListener('click', function(e) {
            e.preventDefault();
            openPrivacyModal();
        });

        privacyModalClose.addEventListener('click', closePrivacyModal);

        privacyModalBackdrop.addEventListener('click', function(e) {
            if (e.target === privacyModalBackdrop) {
                closePrivacyModal();
            }
        });

        // Close modal on Escape key
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape' && !privacyModal.classList.contains('hidden')) {
                closePrivacyModal();
            }
        });
    });
</script>
