<script>
    // Terms of Service Modal functionality
    document.addEventListener('DOMContentLoaded', function() {
        const tosModal = document.getElementById('tosModal');
        const tosLink = document.getElementById('tosLink');
        const tosModalClose = document.getElementById('tosModalClose');
        const tosModalBackdrop = document.getElementById('tosModalBackdrop');

        if (!tosModal || !tosLink || !tosModalClose || !tosModalBackdrop) {
            return;
        }

        function openTosModal() {
            tosModal.classList.remove('hidden');
            document.body.style.overflow = 'hidden';
        }

        function closeTosModal() {
            tosModal.classList.add('hidden');
            document.body.style.overflow = '';
        }

        tosLink.addEventListener('click', function(e) {
            e.preventDefault();
            openTosModal();
        });

        tosModalClose.addEventListener('click', closeTosModal);

        tosModalBackdrop.addEventListener('click', function(e) {
            if (e.target === tosModalBackdrop) {
                closeTosModal();
            }
        });

        // Close modal on Escape key
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape' && !tosModal.classList.contains('hidden')) {
                closeTosModal();
            }
        });
    });
</script>
